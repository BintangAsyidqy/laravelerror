    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|min:3',
            'type' => 'required',
            'price' => 'required|numeric',
            'stock' => 'required|numeric',
        ]);

        Medicine::create([
            'name' => $request->$name,
            'type' => $request->$type,
            'price' => $request->$price,
            'stock' => $request->$stock,
        ]);

        return redirect()->back()->with('succes', 'Berhasil Menambahkan Data Obat!');
